        RSS Stands for Really Simple Syndication. RSS is a convenient way for websites to make their data available to other developers who can freely or with a subscription integrate it into their own websites. The goal of placing an RSS feed on your website is to make your content reach more viewers by allowing other developers to make use of it.
    </p>
    <p  class="text-justify">
        RSS Feeds are essentially a publicly available Text files. This text file contains all the data you wish to be available to other developers. And RSS feeds data is stored XML format. the feed needs to be read by the receiving websites and deserialized by it so that it can be displayed on their website in whatever way they choose. The host of the RSS feed can dictate what data is published to the feed but the 3rd party has full control of the layout of the provided data as it will be displayed on their website.
    </p>

