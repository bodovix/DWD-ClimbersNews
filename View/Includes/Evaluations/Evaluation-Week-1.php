    <h2>HTML frameworks – comparison of grid systems</h2>
    <p class="text-justify">There are many different HTML frameworks available to develop a website against such as Bootstrap and Pure.CSS. Bootstrap is an Open Source originally created by Twitter and Pure.CSS is another OS Framework by Yahoo.
        Both frameworks work under a grid system. Grid systems are a way of splitting the page into rows and columns to allow for quick setup of mobile responsive websites.
        Bootstrap and Pure handle their grid systems differently. Bootstrap uses a 12 Column grid meaning the narrowest fraction of the screen a division can take up is 1/12th of the screen whereas Pure uses a more granular 1/5th or 1/24th grid.
        Bootstrap was implemented in the site as working in 1/12th’s was the best compromise between functionality and usability. Whereas Pure.CSS’s 1/5th would be too ridged and 1/24th’s would be unnecessarily complicated.
    </p>

    <h2>Bootstrap menu system</h2>
    <p class="text-justify">Developing multi-layer menus from scratch is surprisingly complicated and almost guaranteed to waste hours of development time. Bootstrap avoids this pitfall by making use of its readymade responsive styles that have been preconfigured to be fully mobile responsive and allow for easy implementations of menus.	</p>
